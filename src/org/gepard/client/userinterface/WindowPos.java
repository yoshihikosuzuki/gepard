package org.gepard.client.userinterface;

// represents the position of a GUI window

public class WindowPos {
	
	public int width;
	public int height;
	public int x;
	public int y;
	
	public WindowPos(int width, int height, int x, int y) {
		this.width = width;
		this.height = height;
		this.x = x;
		this.y = y;
	}

}
